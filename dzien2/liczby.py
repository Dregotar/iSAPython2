# to jest liczba 5
print(5)

# to jest string z cyfrą 5
# string to ciąg znaków
print("5")
print('5')

print(5 + 5)
print(2 * 3)

# poleceniem type() sprawdzamy typ obiektu
# to jest int czyli liczba całkowita
print(type(2))

# to jest float czyli liczba zmiennoprzecinkowa
print(2.0)
print(type(2.0))

# wynikiem dzielenia jest zawsze float
print(20 / 10)

# funkcja round() służy do zaokrąglania
print(round(15 / 3, 3))
